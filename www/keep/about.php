<h3 align="center">siRNA Selection Program</h3>
This site helps you select siRNAs to knock down your gene of interest. After entering your sequence or accession/gi number, choosing pattern for your oligo nucleotides and various filter methods, you will be presented with a list of oligos matching your criteria. From this list, select those oligos you would like to consider further. All the selected oligos will be BLASTed against RefSeq, UniGene, or Ensembl database.  In the BLAST result page, siRNAs can be further filtered to reduce the non-specific target effects.<p />
Please click <a href="FAQ.html#design">here</a> for rules on designing good siRNAs.
